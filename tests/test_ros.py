def _make_mock_stream(output="image", message="sensor_msgs/CompressedImage"):
    """Build a RosStream-like object bypassing roslibpy entirely."""
    try:
        import roslibpy
    except ImportError:
        pytest.skip("roslibpy not installed")

    with (
        patch("machinevisiontoolbox.Sources.roslibpy.Ros") as MockRos,
        patch("machinevisiontoolbox.Sources.roslibpy.Topic") as MockTopic,
    ):
        MockRos.return_value.run = MagicMock()
        MockTopic.return_value.subscribe = MagicMock()
        stream = RosStream("/test", message=message, output=output)
    stream.client = MagicMock()
    stream._topic = MagicMock()
    return stream


@pytest.mark.skipif(not _roslibpy_importable, reason="roslibpy not installed")
class TestRosStreamCallback(unittest.TestCase):

    def _jpeg_msg(self, r=128, g=100, b=50):
        """Build a minimal roslibpy compressed-image message dict."""
        import base64
        import io
        from PIL import Image as PILImg

        arr = np.full((4, 4, 3), [r, g, b], dtype=np.uint8)
        buf = io.BytesIO()
        PILImg.fromarray(arr, "RGB").save(buf, format="JPEG", quality=95)
        return {
            "header": {"stamp": {"secs": 10, "nsecs": 500_000_000}},
            "format": "jpeg",
            "data": base64.b64encode(buf.getvalue()).decode(),
        }

    def test_image_mode_sets_frame(self):
        stream = _make_mock_stream(output="image")
        stream._process_frame(self._jpeg_msg())
        self.assertIsNotNone(stream._latest_frame)
        self.assertIsNone(stream._latest_message)

    def test_image_mode_timestamp(self):
        stream = _make_mock_stream(output="image")
        stream._process_frame(self._jpeg_msg())
        # secs=10, nsecs=500_000_000 → 10_500_000_000 ns
        self.assertEqual(stream._latest_timestamp, 10_500_000_000)

    def test_message_mode_sets_rosmessage(self):
        stream = _make_mock_stream(
            output="message", message="sensor_msgs/Imu"
        )
        msg = {"header": {"stamp": {"secs": 5, "nsecs": 0}}, "data": "x"}
        stream._process_frame(msg)
        self.assertIsNone(stream._latest_frame)
        self.assertIsInstance(stream._latest_message, RosMessage)

    def test_message_mode_rosmessage_fields(self):
        stream = _make_mock_stream(
            output="message", message="sensor_msgs/Imu"
        )
        payload = {"header": {"stamp": {"secs": 7, "nsecs": 0}}, "val": 3}
        stream._process_frame(payload)
        m = stream._latest_message
        self.assertEqual(m.topic, "/test")
        self.assertEqual(m.msgtype, "sensor_msgs/Imu")
        self.assertEqual(m.timestamp, 7_000_000_000)
        self.assertEqual(m.data, payload)

    def test_zero_stamp_fallback(self):
        stream = _make_mock_stream(output="image")
        before = time.time_ns()
        stream._process_frame(self._jpeg_msg(r=0))
        # Force zero stamp
        msg = {
            "header": {"stamp": {"secs": 0, "nsecs": 0}},
            "format": "jpeg",
            "data": self._jpeg_msg(r=0)["data"],
        }
        stream._process_frame(msg)
        after = time.time_ns()
        self.assertGreaterEqual(stream._latest_timestamp, before)
        self.assertLessEqual(stream._latest_timestamp, after)

    def test_ros2_stamp_field_names(self):
        """Accept ROS2-style 'sec'/'nanosec' stamp fields."""
        stream = _make_mock_stream(output="image")
        import base64
        import io
        from PIL import Image as PILImg

        arr = np.zeros((4, 4, 3), dtype=np.uint8)
        buf = io.BytesIO()
        PILImg.fromarray(arr, "RGB").save(buf, format="JPEG", quality=95)
        msg = {
            "header": {"stamp": {"sec": 3, "nanosec": 200_000_000}},
            "format": "jpeg",
            "data": base64.b64encode(buf.getvalue()).decode(),
        }
        stream._process_frame(msg)
        self.assertEqual(stream._latest_timestamp, 3_200_000_000)

    def test_event_set_after_callback(self):
        stream = _make_mock_stream(output="image")
        self.assertFalse(stream._frame_event.is_set())
        stream._process_frame(self._jpeg_msg())
        self.assertTrue(stream._frame_event.is_set())


