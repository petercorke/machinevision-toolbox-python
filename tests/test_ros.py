class TestRosMessage(unittest.TestCase):

    def _make(self, **kwargs):
        defaults = dict(
            topic="/test", msgtype="std_msgs/msg/String", timestamp=42, data={"data": "hi"}
        )
        defaults.update(kwargs)
        return RosMessage(**defaults)

    def test_fields_accessible(self):
        m = self._make()
        self.assertEqual(m.topic, "/test")
        self.assertEqual(m.msgtype, "std_msgs/msg/String")
        self.assertEqual(m.timestamp, 42)
        self.assertEqual(m.data, {"data": "hi"})

    def test_frozen(self):
        m = self._make()
        with self.assertRaises(Exception):
            m.timestamp = 99  # frozen dataclass


