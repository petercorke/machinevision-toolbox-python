# ---------------------------------------------------------------------------
# SyncRosStreams – pure Python, no ROS needed
# ---------------------------------------------------------------------------

class _FakeItem:
    """Minimal timestamped item for SyncRosStreams tests."""

    def __init__(self, timestamp: int, value: str = ""):
        self.timestamp = timestamp
        self.value = value


def _fake_stream(*timestamps):
    """Iterator yielding _FakeItem objects for the given timestamps."""

    def _gen():
        for t in timestamps:
            yield _FakeItem(t)

    s = MagicMock()
    s._subscribe = True
    s.__enter__ = lambda self: self
    s.__exit__ = MagicMock(return_value=False)
    s.__iter__ = lambda self: _gen()
    return s


class TestSyncRosStreams(unittest.TestCase):

    def test_requires_two_streams(self):
        s = _fake_stream(1)
        with self.assertRaises(ValueError):
            SyncRosStreams([s])

    def test_tolerance_must_be_positive(self):
        s1, s2 = _fake_stream(1), _fake_stream(1)
        with self.assertRaises(ValueError):
            SyncRosStreams([s1, s2], tolerance=0)
        with self.assertRaises(ValueError):
            SyncRosStreams([s1, s2], tolerance=-1)

    def test_publish_only_stream_rejected(self):
        s1 = _fake_stream(1)
        s2 = _fake_stream(1)
        s2._subscribe = False
        with self.assertRaises(ValueError):
            SyncRosStreams([s1, s2])

    def test_exact_match(self):
        t = 1_000_000_000
        s1 = _fake_stream(t)
        s2 = _fake_stream(t)
        sync = SyncRosStreams([s1, s2], tolerance=0.02)
        result = list(sync)
        self.assertEqual(len(result), 1)
        a, b = result[0]
        self.assertEqual(a.timestamp, t)
        self.assertEqual(b.timestamp, t)

    def test_within_tolerance_matches(self):
        tol = 20_000_000   # 20 ms in ns
        t0 = 1_000_000_000
        # stream2 is 10 ms ahead — within 20 ms tolerance
        s1 = _fake_stream(t0)
        s2 = _fake_stream(t0 + 10_000_000)
        sync = SyncRosStreams([s1, s2], tolerance=0.02)
        result = list(sync)
        self.assertEqual(len(result), 1)

    def test_outside_tolerance_drops_oldest(self):
        t0 = 1_000_000_000
        # stream1: t0, t0+50ms; stream2: t0+40ms
        # t0 vs t0+40ms → 40 ms apart > 10 ms tol → drop t0 from s1
        # t0+50ms vs t0+40ms → 10 ms apart ≤ 10 ms tol → match
        s1 = _fake_stream(t0, t0 + 50_000_000)
        s2 = _fake_stream(t0 + 40_000_000)
        sync = SyncRosStreams([s1, s2], tolerance=0.01)
        result = list(sync)
        self.assertEqual(len(result), 1)
        a, b = result[0]
        self.assertEqual(a.timestamp, t0 + 50_000_000)
        self.assertEqual(b.timestamp, t0 + 40_000_000)

    def test_multiple_pairs(self):
        base = 1_000_000_000
        step = 100_000_000
        ts1 = [base + i * step for i in range(3)]
        ts2 = [base + i * step + 5_000_000 for i in range(3)]  # 5 ms offset
        s1 = _fake_stream(*ts1)
        s2 = _fake_stream(*ts2)
        sync = SyncRosStreams([s1, s2], tolerance=0.02)
        result = list(sync)
        self.assertEqual(len(result), 3)

    def test_timestamp_ns_missing_raises(self):
        class NoTimestamp:
            pass

        with self.assertRaises(TypeError):
            SyncRosStreams._timestamp_ns(NoTimestamp())

    def test_repr(self):
        s1, s2 = _fake_stream(1), _fake_stream(1)
        sync = SyncRosStreams([s1, s2], tolerance=0.05)
        r = repr(sync)
        self.assertIn("SyncRosStreams", r)
        self.assertIn("nstreams=2", r)


