@_skip_import
@_skip_bags
class TestRosBagRos1(unittest.TestCase):

    def _bag(self, **kwargs):
        return RosBag(str(ROS1_BAG), release="noetic", **kwargs)

    # --- topics() ---

    def test_topics_returns_all_three(self):
        topics = self._bag(msgfilter=None).topics()
        self.assertIn("/camera/compressed", topics)
        self.assertIn("/camera/raw", topics)
        self.assertIn("/imu/data", topics)

    def test_topics_msgtype(self):
        topics = self._bag(msgfilter=None).topics()
        self.assertIn("CompressedImage", topics["/camera/compressed"])
        self.assertIn("Image", topics["/camera/raw"])
        self.assertIn("Imu", topics["/imu/data"])

    # --- default iteration (msgfilter="Image") ---

    def test_default_yields_images_only(self):
        msgs = list(self._bag())
        self.assertEqual(len(msgs), N_FRAMES * 2)   # compressed + raw
        for m in msgs:
            self.assertIsInstance(m, Image)

    def test_image_shape(self):
        for img in self._bag():
            self.assertEqual(img.shape, (H, W, 3))

    def test_image_timestamp(self):
        imgs = list(self._bag())
        stamps = [img.timestamp for img in imgs]
        # timestamps monotonically increase by STEP (interleaved topics)
        unique_stamps = sorted(set(stamps))
        self.assertEqual(len(unique_stamps), N_FRAMES)
        for i in range(1, N_FRAMES):
            self.assertEqual(unique_stamps[i] - unique_stamps[i - 1], STEP)

    def test_image_topic_attribute(self):
        for img in self._bag():
            self.assertIn(img.topic, {"/camera/compressed", "/camera/raw"})

    # --- topic filter ---

    def test_topicfilter_compressed_only(self):
        msgs = list(self._bag(topicfilter="/camera/compressed"))
        self.assertEqual(len(msgs), N_FRAMES)
        for m in msgs:
            self.assertEqual(m.topic, "/camera/compressed")

    def test_topicfilter_raw_only(self):
        msgs = list(self._bag(topicfilter="/camera/raw"))
        self.assertEqual(len(msgs), N_FRAMES)
        for m in msgs:
            self.assertEqual(m.topic, "/camera/raw")

    def test_topicfilter_list(self):
        msgs = list(
            self._bag(topicfilter=["/camera/compressed", "/camera/raw"])
        )
        self.assertEqual(len(msgs), N_FRAMES * 2)

    # --- message type filter ---

    def test_msgfilter_none_yields_all(self):
        msgs = list(self._bag(msgfilter=None))
        self.assertEqual(len(msgs), N_FRAMES * 3)

    def test_msgfilter_imu_yields_imu(self):
        msgs = list(self._bag(msgfilter="Imu"))
        self.assertEqual(len(msgs), N_FRAMES)
        for m in msgs:
            self.assertNotIsInstance(m, Image)

    def test_imu_passthrough_has_topic_and_timestamp(self):
        msgs = list(self._bag(msgfilter="Imu"))
        for m in msgs:
            self.assertTrue(hasattr(m, "topic"))
            self.assertTrue(hasattr(m, "timestamp"))
            self.assertEqual(m.topic, "/imu/data")

    # --- context manager ---

    def test_context_manager_closes(self):
        bag = self._bag()
        with bag:
            imgs = list(bag)
        # reader should be closed after exiting context
        self.assertIsNone(bag.reader)

    # --- repr / str ---

    def test_repr(self):
        r = repr(self._bag())
        self.assertIn("RosBag", r)
        self.assertIn("noetic", r.lower())

    # --- format helpers ---

    def test_format_duration(self):
        self.assertEqual(RosBag.format_duration(3661_000_000_000), "01:01:01")
        self.assertEqual(RosBag.format_duration(0), "00:00:00")

    def test_format_local_time_returns_string(self):
        s = RosBag.format_local_time(STAMP0)
        self.assertIsInstance(s, str)
        self.assertIn("2023", s)


